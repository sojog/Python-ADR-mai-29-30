# Variabilele sunt case - sensitive
X = "cafea"
print(X)

# mers - variabila
# "mers" - valoare
mers = "rapid"
print(mers)
print("mers")

# snake case - cel preferat pentru nume de variabila
buna_ziua = "Hello"
print(buna_ziua)

# camel case - nu este de preferat notatia, desi functioneaza
bunaZiua = 'Hello'
print(bunaZiua)

# Numele de variabila trebuie sa inceapa cu litera mica
# Litera mare este preferata Programarii orientate obiect - OOP
Varibila = 30
print(Varibila)


